    public void carregarProdutos() {
        try {
            produtos = new ProdutoUC().listar();
            int tamanho = produtos.size();
            String[] colunas = new String[] {"Nome", "Quantidade", "Valor"};
            Object[][] linhas = new Object[tamanho][colunas.length];
            for (int i = 0; i < produtos.size(); i++) {
                linhas[i][0] = produtos.get(i).getNome();
                linhas[i][1] = produtos.get(i).getQuantidade();
                linhas[i][2] = produtos.get(i).getValor();
            }
            tableModel = new DefaultTableModel(linhas, colunas) {
                @Override
                public boolean isCellEditable(int row, int column) {
                    return false;
                }
            };
            jTable1.setModel(tableModel);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

